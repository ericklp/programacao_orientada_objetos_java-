
public class Contato {

	private String nome;
	private Integer id;

	public String toString() {
		return "Contato [nome=" + nome + ", id=" + id + "]";
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	
}
